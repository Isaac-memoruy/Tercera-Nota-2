package semana5;

public class Calculator {
     
    public int sumar (int numA , int numB){
         return numA + numB;
    }
    public int sumar (int numA , int numB , int numbC){
         return numA + numB + numbC;
    }
    public double sumar (double numA , double numB){
         return numA + numB;
    }
}
